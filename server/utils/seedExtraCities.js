const seedExtraCities = async () => {
  try {
    await mongoose.connect(MONGO_URI);
    console.log('MongoDB connected');

    let countriesAdded = 0;
    let citiesAdded = 0;

    for (const countryData of extraCountries) {
      // Check if country already exists
      const existing = await Country.findOne({ code: countryData.code });
      if (existing) {
        console.log(`⏭️  Skipped (exists): ${countryData.name}`);
        continue;
      }

      // Create slug
      const slug = countryData.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
      const country = await Country.create({ ...countryData, slug });
      countriesAdded++;
      console.log(`✅ Added country: ${countryData.name} (${countryData.continent})`);

      // Add cities for this country
      const cityData = extraCitiesByCountry[countryData.name];
      if (cityData) {
        const citiesWithRef = cityData.map(city => ({
          ...city,
          country: country._id,
          slug: city.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, ''),
        }));
        await City.insertMany(citiesWithRef);
        citiesAdded += citiesWithRef.length;
        console.log(`  🏙️  Added ${citiesWithRef.length} cities for ${countryData.name}`);
      }

      // Update country stats
      const cityCount = await City.countDocuments({ country: country._id });
      await Country.findByIdAndUpdate(country._id, { 'stats.cityCount': cityCount });
    }

    const totalCountries = await Country.countDocuments();
    const totalCities = await City.countDocuments();

    console.log('\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
    console.log('📊 SEED EXTRA SUMMARY');
    console.log(`New countries added: ${countriesAdded}`);
    console.log(`New cities added: ${citiesAdded}`);
    console.log(`Total countries now: ${totalCountries}`);
    console.log(`Total cities now: ${totalCities}`);
    console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
    process.exit(0);
  } catch (error) {
    console.error('❌ Error:', error.message);
    process.exit(1);
  }
};

